.386
.MODEL flat,stdcall
.STACK 4096
ExitProcess PROTO, dwExitCode:DWORD

.DATA
	ORG $+20H
	STRG BYTE 'ABCDEFGHIJ'
	LENG EQU $-STRG
	BLOCK1 DWORD STRG
	BLOCK2 DWORD STRG-5

.CODE
MAIN PROC
	MOV CX, LENG
	MOV ESI, BLOCK1
	MOV EDI, BLOCK2
	CMP ESI, EDI
	JA TOP
	ADD ESI, LENG-1
BOTTOM:
	MOV AL, [ESI]
	MOV [EDI], AL
	DEC ESI
	DEC EDI
	DEC CX
	JNE BOTTOM
	JMP END1
TOP:
	MOV AL, [ESI]
	MOV [EDI], AL
	INC ESI
	INC EDI
	DEC CX
	JNE TOP
END1:
	INVOKE ExitProcess,0
MAIN ENDP
END MAIN
